package com.zzti.school.entity;

public class NReview {

    private String news_id;
    private String news_review_time;
    private String news_review_content;
    private String user_id;

    public String getNews_id() {
        return news_id;
    }

    public void setNews_id(String news_id) {
        this.news_id = news_id;
    }

    public String getNews_review_time() {
        return news_review_time;
    }

    public void setNews_review_time(String news_review_time) {
        this.news_review_time = news_review_time;
    }

    public String getNews_review_content() {
        return news_review_content;
    }

    public void setNews_review_content(String news_review_content) {
        this.news_review_content = news_review_content;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }
}
